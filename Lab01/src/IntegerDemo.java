public class IntegerDemo {
     public static void main(String[] args) {
        System.out.println("Maximum int value: " + Integer.MAX_VALUE); // 2147483647
        System.out.println("Minimum int value: " + Integer.MIN_VALUE); // -2147483648
        System.out.println("Maximum byte value: " + Byte.MAX_VALUE); // 127
        System.out.println("Minimum byte value: " + Byte.MIN_VALUE); // -128
        System.out.println("Maximum short value: " + Short.MAX_VALUE); // 32767
        System.out.println("Minimum short value: " + Short.MIN_VALUE); // -32768
        System.out.println("Maximum long value: " + Long.MAX_VALUE); // 9223372036854775807
        System.out.println("Minimum long value: " + Long.MIN_VALUE); // -9223372036854775808
    }
}